										<ul>
											<li>1st gullök</li>
											<li>3st vitlöksklyftor</li>
											<li>1st morot</li>
											<li>400 gram nötfärs</li>
											<li>400 gram krossade tomater</li>
											<li>5st soltorkade tomater</li>
											<li>5st sardellfiléer i olja (kan uteslutas)</li>
